// ============================================================================
// ملف SolarSystem.h - تعريف كلاس النظام الشمسي
// ============================================================================
// هذا الملف يحتوي على تعريف كلاس SolarSystem الذي يمثل النظام الشمسي الكامل
// يتضمن جميع الكواكب والشمس وإدارة المحاكاة
// ============================================================================

// حماية من التضمين المتكرر (Include Guard)
// تمنع تضمين الملف أكثر من مرة في نفس الوحدة
#ifndef SOLARSYSTEM_H
#define SOLARSYSTEM_H

// تضمين ملف تعريف كلاس الكوكب (لأن النظام الشمسي يحتوي على كواكب)
#include "Planet.h"

// ============================================================================
// كلاس النظام الشمسي (SolarSystem Class)
// ============================================================================
// هذا الكلاس يمثل النظام الشمسي الكامل
// يحتوي على جميع الكواكب والشمس وإدارة المحاكاة
// ============================================================================
class SolarSystem {
// ============================================================================
// القسم الخاص (Private Section)
// ============================================================================
// هذه المتغيرات والدوال غير متاحة من خارج الكلاس
// ============================================================================
private:
    // ------------------------------------------------------------------------
    // المتغيرات الأساسية
    // ------------------------------------------------------------------------
    Planet* planets[9];        // مصفوفة من 9 مؤشرات للكواكب (8 كواكب + بلوتو)
    int numPlanets;            // عدد الكواكب الحالي (8 أو 9 حسب حالة بلوتو)
    double animationSpeed;     // سرعة المحاكاة (1.0 = سرعة عادية)
    double time;                // الوقت الحالي في المحاكاة (بالثواني)
    bool showPluto;            // هل يتم إظهار بلوتو؟ (true = ظاهر، false = مخفي)
    
    // ------------------------------------------------------------------------
    // الدوال المساعدة الخاصة (Private Helper Functions)
    // ------------------------------------------------------------------------
    void drawRealisticSun();    // رسم الشمس بمؤثرات بصرية واقعية
    void drawSaturnRings();     // رسم حلقات زحل (محفوظة للتوافق مع الكود القديم)

// ============================================================================
// القسم العام (Public Section)
// ============================================================================
// هذه الدوال متاحة من خارج الكلاس
// ============================================================================
public:
    // ------------------------------------------------------------------------
    // دالة البناء (Constructor)
    // ------------------------------------------------------------------------
    // تقوم بإنشاء النظام الشمسي الكامل
    // تنشئ جميع الكواكب وتهيئها بالبيانات الفلكية الحقيقية
    SolarSystem();
    
    // ------------------------------------------------------------------------
    // دالة الإتلاف (Destructor)
    // ------------------------------------------------------------------------
    // تقوم بحذف النظام الشمسي من الذاكرة
    // تحرر الذاكرة المخصصة لجميع الكواكب
    ~SolarSystem();
    
    // ------------------------------------------------------------------------
    // دوال التحديث والرسم
    // ------------------------------------------------------------------------
    void update(double delta);  // تحديث النظام (تحريك الوقت للأمام)
    void draw();                // رسم النظام الشمسي الكامل (الشمس + الكواكب)
    
    // ------------------------------------------------------------------------
    // دوال التحكم في السرعة
    // ------------------------------------------------------------------------
    void increaseSpeed();       // زيادة سرعة المحاكاة بمقدار 20%
    void decreaseSpeed();       // تقليل سرعة المحاكاة بمقدار 20%
    
    // ------------------------------------------------------------------------
    // دوال Getter - للوصول إلى القيم
    // ------------------------------------------------------------------------
    double getTime() const;              // إرجاع الوقت الحالي في المحاكاة
    double getAnimationSpeed() const;    // إرجاع سرعة الحركة الحالية
    Planet* getPlanet(int index);       // الحصول على مؤشر لكوكب معين
    int getNumPlanets() const;          // إرجاع عدد الكواكب
    
    // ------------------------------------------------------------------------
    // دوال التحكم في بلوتو
    // ------------------------------------------------------------------------
    void togglePluto();         // تبديل إظهار/إخفاء بلوتو
    bool isPlutoVisible() const; // إرجاع حالة ظهور بلوتو
};

// إنهاء حماية التضمين المتكرر
#endif
